# 이 파일은 사이드바 UI를 구성하는 함수를 정의합니다.
# sidebar.py

import streamlit as st

def build_sidebar_ui():
    """사이드바를 빌드합니다."""
    st.header("📖 사용 방법")
    st.markdown("""
    1. **QR 코드 내용** 영역에 변환할 텍스트를 입력하세요
    2. **QR 코드 설정**에서 크기, 오류 보정 레벨, **그리고 모듈 모양**을 조정하세요
    3. **색상 설정**에서 패턴과 배경 색상을 선택하세요
    4. **파일 설정**에서 원하는 파일 형식(PNG/SVG)을 선택하고 파일명을 지정하세요.
    5. **QR 코드 생성** 버튼으로 최종 파일을 다운로드하세요
    """)

    st.markdown("---")

    st.header("💡 용도별 QR 코드 생성 팁")
    st.markdown("""
    - **텍스트**: `QR 코드로 생성할 텍스트를 입력합니다`
    - **웹사이트**: `https://www.example.com`
    - **이메일**: `mailto:user@example.com`
    - **전화번호**: `tel:type=CELL:+82 10-1234-5678`
    - **SMS**: `sms:010-1234-5678`
    - **WiFi**: `WIFI:T:WPA;S:네트워크명(SSID);P:비밀번호;H:false;;`
    """)

    st.markdown("---")

    st.header("⚙️ 설정 가이드")
    st.markdown("""
    **오류 보정 레벨:**
    - **Low (7%)**: 손상되지 않는 환경
    - **Medium (15%)**: 일반적인 사용
    - **Quartile (25%)**: 약간의 손상 가능
    - **High (30%)**: 로고 삽입, 손상이 잦은 환경
    
    **모듈 모양 (PNG 전용):**
    - **기본 사각형 (Square)**: 가장 일반적인 QR 코드 모양입니다.
    - **둥근 사각형 (Rounded)**: 부드러운 모서리로 세련된 느낌을 줍니다.
    - **원형 (Circle)**: 원형 패턴으로 독특하고 현대적인 디자인을 연출합니다.
    
    **마스크 패턴:**
    - 0~7 중 선택 (같은 내용이라도 번호에 따라 패턴이 달라짐)
    **색상 입력:**
    - **HEX 코드**: #FF0000, #00FF00, #0000FF 등
    """)
